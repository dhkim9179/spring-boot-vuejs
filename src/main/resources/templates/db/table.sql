CREATE TABLE IF NOT EXISTS `USER` (
  `USER_ID` VARCHAR(20) NOT NULL COMMENT '사용자ID',
  `USER_PASSWORD` VARCHAR(60) NOT NULL COMMENT '사용자 비밀번호',
  `USER_NAME` VARCHAR(60) NOT NULL COMMENT '사용자명',
  `EMAIL_ADDRESS` VARCHAR(100) NOT NULL COMMENT '이메일주소',
  `MOBILE_PHONE_NUMBER` VARCHAR(20) NOT NULL COMMENT '핸드폰번호',
  `PASSWORD_FAIL_COUNT` INT NULL DEFAULT 0 COMMENT '비밀번호 틀린 횟수',
  `PASSWORD_INIT_YN` VARCHAR(1) NULL DEFAULT 'N' COMMENT '비밀번호 초기화여부',
  `PASSWORD_MODIFY_DATETIME` VARCHAR(8) NULL DEFAULT NULL COMMENT '비밀번호 변경일시',
  `LANGUAGE` VARCHAR(2) NULL DEFAULT NULL COMMENT '사용자언어',
  `USER_STATUS` VARCHAR(1) NULL DEFAULT NULL COMMENT '사용자상태',
  `CREATE_DATETIME` VARCHAR(14) NULL DEFAULT NULL COMMENT '등록일시',
  `CREATE_USER_ID` VARCHAR(20) NULL DEFAULT NULL COMMENT '등록자ID', 
  `UPDATE_DATETIME` VARCHAR(14) NULL DEFAULT NULL COMMENT '수정일시', 
  `UPDATE_USER_ID` VARCHAR(20) NULL DEFAULT NULL COMMENT '수정자ID', 
  PRIMARY KEY (`USER_ID`)
)
ENGINE = InnoDB
COMMENT = '사용자정보';


